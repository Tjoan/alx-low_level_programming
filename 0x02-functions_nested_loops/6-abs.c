/**
* _abs - computes the absolute value of an integer.
*
* Description: computes the absolute value of an integer.
*
* @b: holds the value to be used
*
* Return: Absolute value
*/
int _abs(int b)
{

	if (b < 0)
	{
		return (-b);
	}
	else if (b == 0)
	{
		return (b);
	}
	else
	{
		return (b);
	}
}
